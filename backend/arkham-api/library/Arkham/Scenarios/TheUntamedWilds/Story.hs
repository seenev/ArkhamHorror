      \ this discovery defies even his broad-minded theories. I suppose he has a right\
      \ to be distrustful. Nothing about this expedition makes sense."
    , "We have cleared a route to what Alejandro believes is the main temple of the\
      \ ruins. There is little time to rest. The serpent creatures seem intent on\
      \ stopping us, so we cannot tarry any longer than necessary. Soon we will enter\
