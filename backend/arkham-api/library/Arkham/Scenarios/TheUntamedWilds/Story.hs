  FlavorText
    (Just "No Resolution")
    [ "Wednesday, July 1st, 1925"
    , "I still have no idea how we made it back to camp alive.  The jungle was more\
      \ perilous than we could have possibly imagined. This may read like the ravings\
      \ of a lunatic, but I cannot explain it any other way. Being lost in the jungle\
      \ was no picnic, but that was the least of our worries. The rainforest was\
      \ guarded. Guarded by unfathomable horrors: serpentine creatures driven by anger\
      \ and hate, hunting us relentlessly."
    , "Perhaps just as deadly were the human natives of the rainforest. They appeared\
      \ to be Aztec warriors as far as dress and speech were concerned. Despite the\
      \ conquering of the Aztec people hundreds of years ago, these warriors somehow\
      \ survived. How they managed to remain undiscovered this long is beyond us. I\
      \ thought Alejandro would be excited to learn more about them, but he was more\
      \ disturbed than fascinated. I suppose he has a right to be incredulous. Nothing\
      \ about this expedition makes sense."
    , "The serpent creatures chased us for what seemed like days. Finally we were able\
      \ to find our way back to camp, but the price was steep. We had lost all progress\
      \ we had made, and many of us were injured or afflicted by serpent venom. We have\
      \ no choice but to send runners to the nearest city for additional supplies and\
      \ to wait and recover from our wounds."
    ]

